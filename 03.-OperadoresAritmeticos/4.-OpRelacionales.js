
//Operadores Relacionales (Comparacion)
let a = 5;
let b = 6;
console.log(a)
console.log(b)

//Operadores igualdad
//(solo compara valores, y hace conversion si es necesario)
console.log("a == b -> ", a == b)
console.log(`${a} == ${b} -> ${a == b}`)

//Operador igualdad Estricta o Exacta
//(compara el valor y el tipo de dato) ===
console.log(`${a} === ${b} -> ${a === b}`)

//Operador distinto
console.log(`${a} != ${b} -> ${a != b}`)

//Operador distinto exacto
console.log(`${a} !== ${b} -> ${a !== b}`)

//Operador Menor que
console.log(`${a} < ${b} -> ${a < b}`)

//Operador Memor o Igual que
console.log(`${a} <= ${b} -> ${a <= b}`)

//Operador Mayor que
console.log(`${a} > ${b} -> ${a > b}`)

//Operador Mayor o Igual que
console.log(`${a} >= ${b} -> ${a >= b}`)

